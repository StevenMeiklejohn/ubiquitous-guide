class Room

  attr_accessor :room_number, :room_type, :room_cost, :status, :occupier_name

def initialize(room_number, room_type, room_cost, status, occupier_name)
  @room_number = room_number
  @room_type = room_type
  @room_cost = room_cost
  @status = status
  @occupier_name = occupier_name
end




end
